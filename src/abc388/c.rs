fn main() {
    input! {
        n: usize,
        mut a: [usize; n],
    }
    a.sort();
    let mut ans = 0;
    for i in 0..n-1 {
        let ind = a.lower_bound(&(2*a[i]));
        ans += n - ind;
    }
    println!("{}", ans);

}